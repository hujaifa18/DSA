#include <iostream>
using namespace std;
int main()
{
    cout << (3 < 5) << endl;  // true
    cout << (3 > 5) << endl;  // false
    cout << (3 <= 3) << endl; // true
    cout << (3 >= 3) << endl; // true
    cout << (3 == 3) << endl; // true
    cout << (3 != 3) << endl; // false
    return 0;
}